using System;

namespace Bai3_OperatorInCsharp
{
    class Program
    {
        static void Main(string[] args)
        {
            // Operator + - x % 
            float a = 5 ;
            int b = 4 ; 
            Console.WriteLine("a + b = {0}",a +b )  ;
            Console.WriteLine("a - b = {0}",a - b )  ;
            Console.WriteLine("a * b = {0}",a *b )  ;
            Console.WriteLine("a / b = {0}",a /b )  ;
            Console.WriteLine("a % b = {0}",a % b )  ;
        }
    }
}
